import { Model, Schema } from "mongoose";
import { TUser } from "./user.interface";

const userSchema = new Schema({
id: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  age: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
})


const user = Model<TUser>('User', userSchema);

export default user;